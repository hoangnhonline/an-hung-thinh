<p style="text-align:justify"><span style="background-color:transparent; font-family:arial,helvetica,tahoma,sans-serif; font-size:13px">Xét về độ cứng :Đá Marble thường không có độ cứng cao như đá Granite,nhưng bù lại Đá Marble có vân tự nhiên sống động và tươi sắc hơn bất kì loại đá nào.</span><span style="background-color:transparent; font-family:arial,helvetica,tahoma,sans-serif; font-size:13px">Vì thế, nếu không xử lý chống thấm kỹ càng về lâu dài sẽ thấm nước, dính bụi bẩn và chuyển màu, mất đi vẻ đẹp ban đầu. Ngoài ra về mặt thi công cũng khó hơn vì rất dễ vỡ, mẻ và khó tìm sản phẩm tương đồng nên giá thi công cao hơn so với đá Granite. Tuy nhiên, vì vẻ đẹp sang trọng, gam màu lạnh nhưng trang nhã của đá sẽ mang lại cho gia chủ sự thư thái, mát dịu, sang trọng. Đá Marble thường có giá cao hơn Granite và thường lát vị trí đặc biệt như cầu thang máy, ốp tường, sàn sảnh đón…</span><span style="background-color:transparent; font-family:arial,helvetica,tahoma,sans-serif; font-size:13px">&nbsp;</span></p>

