<p><span style="color:#FF0000"><strong>Về Đá Granite:</strong></span><span style="font-family:arial,helvetica,tahoma,sans-serif; font-size:13px">&nbsp;</span><span style="font-family:arial,helvetica,tahoma,sans-serif; font-size:13px">hay gọi là đá hoa cương, là loại đá tự nhiên được hình thành do sự biến chất của các loại đá vôi, đá carbonate hay đá dolomit. Quá trình biến chất tái tinh thể hóa các loại đá trầm tích này để thành các vân tinh thể đá xen kẽ lẫn nhau .</span></p>

<p><span style="font-family:arial,helvetica,tahoma,sans-serif; font-size:13px">Vì kết cấu tinh thể, đá Granite có độ cứng cao, ít thấm nước, có thể lát ở các vị trí chịu xước, mài mòn như cầu thang bộ, sàn nhà, mặt tiền…Thường vì giá thành thấp hơn marble, dễ thi công hơn vì ít gãy, mẻ nên nhìn chung đá Granite thường được dùng rộng rãi. Việt Nam rất nổi tiếng với các mỏ đá ở miền Trung. Khí hậu ở đây nóng nên đá granite ở đây có độ bền về cơ học, màu sắc rất cao ngay cả với các công trình ở chịu nắng gió. So với các loại đá Granite ở các nước khác, điển hình là Trung Quốc thì chất lượng vượt trội về độ cứng, bền màu và được các nước trên thế giới đánh giá cao.</span></p>

